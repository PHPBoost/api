			<div class="description">
				<p>This class is responsible to load a template and transform it in the PHPBoost parsed template
syntax.
Normally a loader is able to load a template and provide its parsed form. The parsing is done by
a TemplateParser it embeds and is a quite heavy operation. To be more efficient, we've introduced
the parsed files caching which enables us to parse each file only once and then reuse the cache.
Even if the <code><a href="class-TemplateLoader.php">TemplateLoader</a></code> interface knows the cache notion, that doesn't mean that the
implementation has to support caching. It's the reason why there is the <code><a href="class-TemplateLoader.php#_supports_caching">TemplateLoader::supports_caching()</a></code> method.</p>
