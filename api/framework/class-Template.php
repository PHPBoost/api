			<div class="description">
				<p>This class represents a PHPBoost template. Templates are used to generate text which
have a common structure. You just have to write your text with tags at the place you want to insert
values and assign values in the objet, when you will display them, the tags will be replaced by
the corresponding value.
PHPBoost's template engine is home made and has its own syntax which is the described below:</p>

<h4>Variable assignation</h4>

<h5>Simple variables</h5>

<p>A simple variable is accessible with the {NAME} syntax where NAME is its template name. If the variable is not assigned, nothing will be displayed (no error message).
Simple variables are assigned by the assign_vars() method.</p>

<h4>Lang variables</h4>

<p>To speed the development up, you can avoid setting all the localized variables to use. You just have to associate one or more lang map
(lang_identifier => localized lang) with the <code><a href="class-Template.php#_add_lang">Template::add_lang()</a></code> method. In the template, you just have to
add the 'L_' prefix before the variable name and it will be searched in the langs map.</p>

<h5>Loops</h5>

<p>You can make some loops to repeat a pattern, those loops can be nested. A loop has a name (name) and each iteration contains some variables, for example, the variable VAR.
<br />&#35; START name #
<br />My variable is {name.VAR}
<br />&#35; END name #
<br />To nest loops, here is an example:
<br />&#35; START loop1 #
<br />I write my loop1 var here: {loop1.VAR}.
<br />&#35; START loop1.loop2 #
<br />I can write my loop2 var here: {loop1.loop2.VAR} but also my loop1 var of the parent loop: {loop1.VAR}.
<br />&#35; END loop1.loop2 #
<br />&#35; END loop1 #
To assign the variable, see the assign_block_vars() method which creates one iteration.</p>

<h5>Conditions</h5>

<p>When you want to display something only in particular case, you can use some condition tests.
<br />&#35; IF C_MY_TEST #
<br />This text will be displayed only if the C_MY_TEST variable is true
<br />&#35; ENDIF #
You can nest some conditions.</p>

<h3>Nesting templates</h3>

<p>You can embed a template in another one. For that, you have to use the INCLUDE instruction like that: # INCLUDE template #
where template is the identifier of a template added with the add_subtemplate() method. When the template will be displayed, this
instruction will be replaced by the content of the template you have attached to this identifier or nothing if the template hasn't been set.
You also can include templates in a loop, for that you have to place them in the third parameter of the asssign_block_vars() method.</p>

<h4>Variables using</h4>

<p>By default, when you call the variable by it's name, it'll be returnes as it was assigned. But you can do little implicit treatments of variables
directly in the template. Here are the prefixes to use before the variable names:</p>

<ul>
<li><E_: the variable will be searched without its prefix and will be escaped using <pre><span class="php-keyword2">htmlspecialchars</span>()</pre></li>
<li><J_: the variable will be searched without its prefix and will be escaped using <pre>TextHelper::to_js_string()</pre></li>
<li><L_: the variable will be searched without its prefix in every languages maps registered using <pre>Template-&gt;add_lang(<span class="php-var">$language</span>)</pre></li>
<li><EL_: the variable will be searched without its prefix like languages variables and will be escaped using <pre><span class="php-keyword2">htmlspecialchars</span>()</pre></li>
<li><JL_: the variable will be searched without its prefix like languages variables and will be escaped using <pre>TextHelper::to_js_string()</pre></li>
</ul>
